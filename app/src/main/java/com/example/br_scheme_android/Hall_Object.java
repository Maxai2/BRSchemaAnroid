package com.example.br_scheme_android;

public class Hall_Object {

    public String Key;
    public String Src;
    public int Angle;
    public String Pos;
    public String Group;
    public String Size;
    public String Figure;

    public Hall_Object(String Key, String Src, int Angle, String Pos, String Group, String Size, String Figure) {
        this.Key = Key;
        this.Src = Src;
        this.Angle = Angle;
        this.Pos = Pos;
        this.Group = Group;
        this.Size = Size;
        this.Figure = Figure;
    }
}
